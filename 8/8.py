"""1 номер. 1 способ"""
c = 0
res = 0
for x in 'АЖИМНУЧ':
    for y in 'АЖИМНУЧ':
        for z in 'АЖИМНУЧ':
            for w in 'АЖИМНУЧ':
                for e in 'АЖИМНУЧ':
                    for k in 'АЖИМНУЧ':
                        c += 1
                        s = x + y + z + w + e + k
                        if c % 2 != 0 and s[0] != 'Ж' and s.count('Ч') <= 1:
                            res += 1
print(res)

"""1 номер. 2 способ (Лаконичнее)"""
from itertools import product
c = 0
res = 0
for x, y, z, w, e, k in product('АЖИМНУЧ', repeat=6):
    c += 1
    s = x + y + z + w + e + k
    if c % 2 != 0 and s[0] != 'Ж' and s.count('Ч') <= 1:
        res += 1
print(res)

"""2 номер"""
from itertools import product
c = 0
# product генерирует все возможные комбинации (размещения с повторениями)
# длиной 5 (repeat=5) из чисел от 0 до 15.
# По сути, мы перебираем все 5-значные числа в 16-ричной системе счисления.
for x in product(range(16), repeat=5):

    # Разбираем условия в if:
    # 1. x[0] != 0
    #    -> Число не может начинаться с нуля (иначе оно станет 4-значным).
    # 2. any(i**0.5 == int(i**0.5) for i in x)
    #    -> Функция any() вернет True, если хотя бы для одной цифры (i) в числе (x)
    #       корень из нее равен целому числу (то есть цифра является полным квадратом: 0, 1, 4 или 9).
    # 3. all(x.count(i) <= 2 for i in x)
    #    -> Функция all() вернет True, если КАЖДАЯ цифра встречается в числе не более 2 раз.
    if x[0] != 0 and any(i**0.5 == int(i**0.5) for i in x) and all(x.count(i) <= 2 for i in x):
        c += 1
print(c)

"""3 номер"""
from itertools import product
c = 0
# product генерирует все возможные "слова" длиной 6 (repeat=6) из заданных букв.
for x in product('КАЛИЙ', repeat=6):
    # product выдает результат в виде кортежа, например: ('К', 'А', 'Л', 'И', 'Й', 'К').
    # Метод ''.join(x) склеивает эти буквы в сплошную строку 'КАЛИЙК',
    # чтобы нам было удобно использовать строковые методы поиска (in, count).
    s = ''.join(x)

    # Проверяем ограничения задачи:
    # 1. s.count('Й') <= 1
    #    -> Буква 'Й' встречается в слове не более одного раза (0 или 1 раз).
    # 2. s[0] != 'Й' and s[-1] != 'Й'
    #    -> Буква 'Й' не стоит на первом (индекс 0) и на последнем (индекс -1) месте.
    # 3. 'ИЙ' not in s and 'ЙИ' not in s
    #    -> В строке нет сочетаний 'ИЙ' и 'ЙИ', то есть 'Й' не стоит рядом с 'И'.
    if s.count('Й') <= 1 and s[0] != 'Й' and s[-1] != 'Й' and 'ИЙ' not in s and 'ЙИ' not in s:
        c += 1
print(c)

"""4 номер"""
from itertools import permutations
sogl = 'ТМШВСК'
gls = 'ИАЕ'
c = 0

for p in permutations('ТИМАШЕВСК'):
    word = "".join(p)

    # Проверяем согласные по краям
    if word[0] in sogl and word[-1] in sogl:

        # Создаем маску слова (например, 'СГСГГСССС')
        mask = ""
        for letter in word:
            if letter in gls:
                mask += "Г"
            else:
                mask += "С"

        # Проверяем условие "ровно две гласные рядом"
        # Если есть 'ГГ', но нет 'ГГГ', значит третья гласная стоит отдельно
        if 'ГГ' in mask and 'ГГГ' not in mask:
            c += 1
print(c)
