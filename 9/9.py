# Переменная-счетчик, в которой мы будем хранить количество подходящих строк
c = 0
# Открываем файл (предполагается, что данные из Excel скопированы в txt)
# и перебираем его построчно
for line in open('chicla.txt'):
    # Превращаем текстовую строку (например, "12 45 12 12 7 9 10")
    # в список целых чисел: [12, 45, 12, 12, 7, 9, 10]
    a = [int(i) for i in line.split()]

    # 1. Готовим данные для первого условия:
    # Собираем в список p3 все числа, которые встречаются в строке ровно 3 раза.
    # Если такое число одно (например, 12), генератор добавит его трижды: [12, 12, 12]
    p3 = [i for i in a if a.count(i) == 3]

    # Собираем в список n все числа, которые встречаются ровно 1 раз (уникальные).
    # Для нашего примера это будут: [45, 7, 9, 10]
    n = [i for i in a if a.count(i) == 1]

    # 2. Готовим данные для второго условия:
    # Находим максимальное число в текущей строке (в списке 'a')
    mx = max(a)

    # Проверяем ПЕРВОЕ условие: "одно число повторяется трижды, остальные различны".
    # В строке 7 чисел. Если одно повторяется трижды (длина p3 равна 3),
    # а остальные уникальны, то их должно быть ровно 4 штуки (длина n равна 4).
    # 3 + 4 = 7 чисел всего.
    if len(p3) == 3 and len(n) == 4:

        # Проверяем ВТОРОЕ условие: "максимальное число строки не повторяется".
        # Это значит, что максимум (mx) должен находиться среди уникальных чисел (в списке n).
        # Альтернативный и тоже понятный вариант: if a.count(mx) == 1:
        if mx in n:

            # Если оба условия выполнились, увеличиваем счетчик подходящих строк
            c += 1

# Выводим итоговый результат
print(c)